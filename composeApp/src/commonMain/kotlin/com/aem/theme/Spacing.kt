package com.aem.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing tokens for consistent layout throughout the app.
 * Based on an 8dp grid system.
 */
object Spacing {
    /** Extra small spacing: 4dp */
    val xs = 4.dp

    /** Small spacing: 8dp */
    val sm = 8.dp

    /** Medium spacing: 16dp */
    val md = 16.dp

    /** Large spacing: 24dp */
    val lg = 24.dp

    /** Extra large spacing: 32dp */
    val xl = 32.dp

    /** Double extra large spacing: 48dp */
    val xxl = 48.dp

    /** Triple extra large spacing: 64dp */
    val xxxl = 64.dp
}

/**
 * Icon sizes for consistent iconography
 */
object IconSize {
    /** Small icon: 16dp */
    val sm = 16.dp

    /** Medium icon: 24dp (default) */
    val md = 24.dp

    /** Large icon: 32dp */
    val lg = 32.dp

    /** Extra large icon: 48dp */
    val xl = 48.dp
}

/**
 * Border radius tokens for consistent corner rounding
 */
object CornerRadius {
    /** No rounding: 0dp */
    val none = 0.dp

    /** Small rounding: 4dp */
    val sm = 4.dp

    /** Medium rounding: 8dp */
    val md = 8.dp

    /** Large rounding: 12dp */
    val lg = 12.dp

    /** Extra large rounding: 16dp */
    val xl = 16.dp

    /** Full rounding (for pills): 999dp */
    val full = 999.dp
}

